#Python Operators

x = 50
y = 2
#Arithmetic operators - + - * / % ** //

print("--------------------------")
print("+ of 50 and 2 is : ")
print(x + y)
print("--------------------------")

print("- of 50 and 2 is : ")
print(x - y)
print("--------------------------")

print("* of 50 and 2 is : ")
print(x * y)
print("--------------------------")

print("/ of 50 and 2 is : ")
print(x / y)
print("--------------------------")

print("% of 50 and 2 is : ")
print(x % y)
print("--------------------------")

print("** of 50 and 2 is : ")
print(x ** y)
print("--------------------------")

#floor division // rounds the result down to the nearest whole number
print("// of 50 and 2 is : ")
print(x // y)
print("--------------------------")