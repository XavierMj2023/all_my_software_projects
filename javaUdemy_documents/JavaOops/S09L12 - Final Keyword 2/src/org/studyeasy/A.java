package org.studyeasy;

//Parent Class
public class A {
	//This method is Normal Method
	public void india1() {
		System.out.println("India is amazing");
	}
	
	//This method is Normal Method
	public void USA2() {
		System.out.println("USA is amazing");
	}
	
	//This method is Final Method
	public final void india() {
		System.out.println("India is amazing");
	}
	
	//This method is Final Method
	public final void USA() {
		System.out.println("USA is amazing");
	}
}
