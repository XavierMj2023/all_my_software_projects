# Python Operators

data = ["Salvador", "Leo", "Martina", "RTruth"]
# Membership operators - in and not in
print("Leo" in data)  # true
print("Leo" not in data)  # false

print("---------------------------")

print("Miz" in data)  # false
print("Miz" not in data)  # true

print("---------------------------")

print("leo" in data)  # false
print("leo " not in data)  # true

print("---------------------------")

print("leo " in data)
print("leo " not in data)
