# Python Operators

x = ["Manoj", "Deshpandy"]
y = ["Manoj", "Deshpande"]
z = y
# Identity operators - is and is not
# Identity operators are used to compare the objects, not if they are equal, but if they are actually the
# same object, with the same memory location.
print(x == y)
print(x is y)
print(x != y)
print(x is not y)
print(x is z)


